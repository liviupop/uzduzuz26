  // Replace the contents of an existing .note element in place, preserving
  // its identity in the DOM so the CSS flex transition can animate the width
  // change between spine and active states.
  function rebuildNoteContent(noteEl, id, depth, isActive) {
    const fresh = renderNote(id, depth, isActive);
    if (!fresh) return;
    noteEl.className = fresh.className;
    Array.from(noteEl.attributes).forEach(a => {
      if (a.name === 'class' || a.name === 'style') return;
      noteEl.removeAttribute(a.name);
    });
    Array.from(fresh.attributes).forEach(a => {
      if (a.name === 'class' || a.name === 'style') return;
      noteEl.setAttribute(a.name, a.value);
    });
    noteEl.innerHTML = '';
    while (fresh.firstChild) noteEl.appendChild(fresh.firstChild);
  }

  const app = {
    stack: [],
    activeIndex: -1,
    _prevStack: [],

    render(opts) {
      const historyMode = (opts && opts.history) || 'push';
      // Keep activeIndex in range.
      if (this.activeIndex < 0 || this.activeIndex >= this.stack.length) {
        this.activeIndex = this.stack.length - 1;
      }

      const sameStack = arraysEqual(this.stack, this._prevStack);
      const isInitial = this._prevStack.length === 0;

      if (sameStack && !isInitial) {
        // Stack array is identical (only the active column changed). Rebuild
        // each .note in place; the flex-basis transition animates the width
        // change from spine (64px) to active (1fr) and vice versa.
        const noteEls = Array.from($stackInner.querySelectorAll('.note'));
        noteEls.forEach((noteEl, i) => {
          rebuildNoteContent(noteEl, this.stack[i], i, i === this.activeIndex);
        });
      } else {
        // Stack contents changed: full rebuild.
        $stackInner.innerHTML = '';
        this.stack.forEach((id, i) => {
          const node = renderNote(id, i, i === this.activeIndex);
          if (node) $stackInner.appendChild(node);
        });
      }
      // Mobile bookkeeping: every non-active note pins to the bottom of the
      // viewport via position:fixed with a per-spine `bottom` offset.
      // --spine-index = 0 for the bottom-most strip, growing upward.
      // --spine-count is set on stack-inner so the active note's height and
      // the page's bottom padding can subtract that band.
      const children = Array.from($stackInner.children);
      const spineEls = children.filter((_, i) => i !== this.activeIndex);
      const spineCount = spineEls.length;
      $stackInner.style.setProperty('--spine-count', String(spineCount));
      spineEls.forEach((noteEl, j) => {
        // The last spine in DOM order pins to the bottom (--spine-index=0);
        // earlier spines stack above it.
        noteEl.style.setProperty('--spine-index', String(spineCount - 1 - j));
      });
      this._prevStack = this.stack.slice();

      this.renderChromeNav();
      if (historyMode !== 'none') setStackUrl(this.stack, this.activeIndex, historyMode);
      // Trigger async loads for any unresolved ids; re-render without writing
      // history again when they arrive.
      this.stack.forEach(id => {
        if (!getNote(id) && !MISSING.has(id)) {
          loadNote(id).then(() => this.render({ history: 'none' }));
        }
      });
    },

    renderChromeNav() {
      $chromeNav.innerHTML = '';
      if ($mobileMenu) $mobileMenu.innerHTML = '';
      // Only the currently-active column marks its tab as active.
      const activeId = this.stack[this.activeIndex];
      const goToTab = (t) => {
        // If the page is already in the stack, just promote it to active
        // (no duplicate column). The flex transition slides its column from
        // spine width (64px) to fill, and the previously-active column
        // shrinks to a spine in place.
        const existing = this.stack.indexOf(t.id);
        if (existing !== -1) {
          if (existing !== this.activeIndex) {
            this.activeIndex = existing;
            this.render();
            scrollActiveIntoViewOnMobile();
          }
          return;
        }
        // Otherwise: replace the stack with this tab as the only column.
        this.stack = [t.id];
        this.activeIndex = 0;
        this.render();
        scrollActiveIntoViewOnMobile();
      };
      for (const t of PRIMARY_TABS) {
        const btn = el('button', {
          class: t.id === activeId ? 'is-active' : '',
        }, t.label);
        btn.addEventListener('click', () => goToTab(t));
        $chromeNav.appendChild(btn);

        if ($mobileMenu) {
          const mobileBtn = el('button', {
            class: 'mobile-menu-item' + (t.id === activeId ? ' is-active' : ''),
          }, [
            solarIcon(t.icon),
            el('span', {}, t.label),
          ]);
          mobileBtn.addEventListener('click', () => {
            closeMobileMenu();
            goToTab(t);
          });
          $mobileMenu.appendChild(mobileBtn);
        }
      }
    },

    // Open a note triggered from a link inside the column at fromDepth.
    // If the target is already somewhere in the stack, just promote it to
    // active (no duplicate). Otherwise, push it after fromDepth.
    openNoteAt(targetId, fromDepth) {
      const existing = this.stack.indexOf(targetId);
      if (existing !== -1) {
        if (existing !== this.activeIndex) {
          this.activeIndex = existing;
          this.render();
          scrollActiveIntoViewOnMobile();
        }
        return;
      }
      // Truncate to fromDepth + 1 so the new note replaces anything that was
      // to the right of the column the user clicked from.
      this.stack = this.stack.slice(0, fromDepth + 1);
      this.stack.push(targetId);
      this.activeIndex = this.stack.length - 1;
      this.render();
      scrollActiveIntoViewOnMobile();
    },

    // Click on a spine (any column that isn't currently active): make it the
    // active column. The previously-active column collapses to a spine and
    // stays in the stack at its original position. Nothing is dropped.
    activateAt(depth) {
      if (depth < 0 || depth >= this.stack.length) return;
      if (depth === this.activeIndex) return;
      this.activeIndex = depth;
      this.render();
      scrollActiveIntoViewOnMobile();
    },

    // Close × on any column (active or spine): remove just that one column
    // and adjust activeIndex. The other columns keep their positions.
    closeOne(depth) {
      if (depth < 0 || depth >= this.stack.length) return;
      this.stack.splice(depth, 1);
      if (this.stack.length === 0) {
        this.stack = ['home'];
        this.activeIndex = 0;
      } else {
        if (depth < this.activeIndex) this.activeIndex--;
        else if (depth === this.activeIndex) this.activeIndex = Math.min(depth, this.stack.length - 1);
        if (this.activeIndex >= this.stack.length) this.activeIndex = this.stack.length - 1;
        if (this.activeIndex < 0) this.activeIndex = 0;
      }
      this.render();
    },
  };

  function closeMobileMenu() {
    if (!$mobileMenu || !$mobileMenuToggle) return;
    $mobileMenu.hidden = true;
    $mobileMenuToggle.setAttribute('aria-expanded', 'false');
    $mobileMenuToggle.setAttribute('aria-label', 'open menu');
  }

  function openMobileMenu() {
    if (!$mobileMenu || !$mobileMenuToggle) return;
    $mobileMenu.hidden = false;
    $mobileMenuToggle.setAttribute('aria-expanded', 'true');
    $mobileMenuToggle.setAttribute('aria-label', 'close menu');
  }

  function toggleMobileMenu() {
    if (!$mobileMenu || !$mobileMenuToggle) return;
    if ($mobileMenu.hidden) openMobileMenu();
    else closeMobileMenu();
  }

  if ($mobileMenuToggle && $mobileMenu) {
    closeMobileMenu();
    $mobileMenuToggle.addEventListener('click', (e) => {
      e.stopPropagation();
      toggleMobileMenu();
    });
    $mobileMenu.addEventListener('click', (e) => e.stopPropagation());
    document.addEventListener('click', () => closeMobileMenu());
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape') closeMobileMenu();
    });
    window.addEventListener('resize', () => {
      if (!window.matchMedia || !window.matchMedia('(max-width: 720px)').matches) {
        closeMobileMenu();
      }
    });
  }

  // Delegated handler for spine clicks. Survives in-place rebuilds, since
  // rebuildNoteContent recreates child elements but keeps the .note wrapper.
  $stackInner.addEventListener('click', (e) => {
    const sp = e.target.closest('.note.is-spine');
    if (!sp || !$stackInner.contains(sp)) return;
    // The spine × button has its own listener; ignore clicks on it.
    if (e.target.closest('.spine-close')) return;
    const depth = Array.from($stackInner.children).indexOf(sp);
    if (depth >= 0) app.activateAt(depth);
  });

  // brand link → reset to home (or just refocus home if it's in the stack).
  document.querySelector('.brand').addEventListener('click', () => {
    const existing = app.stack.indexOf('home');
    if (existing !== -1) {
      if (existing !== app.activeIndex) { app.activeIndex = existing; app.render(); }
      return;
    }
    app.stack = ['home'];
    app.activeIndex = 0;
    app.render();
  });

  // boot — replace, not push (no duplicate first entry).
  {
    const init = getStackFromUrl();
    app.stack = init.stack;
    app.activeIndex = init.activeIndex;
    app.render({ history: 'replace' });
  }

  // Browser back/forward: read the URL the browser already restored, render
  // with no history write (URL is already correct).
  window.addEventListener('popstate', () => {
    const next = getStackFromUrl();
    app.stack = next.stack;
    app.activeIndex = next.activeIndex;
    app.render({ history: 'none' });
  });

