  function loadNote(id) {
    id = resolveSlug(id);
    if (NOTES[id]) return Promise.resolve(NOTES[id]);
    if (NOTE_CACHE.has(id)) return Promise.resolve(NOTE_CACHE.get(id));
    if (MISSING.has(id)) return Promise.resolve(null);
    if (PENDING.has(id)) return PENDING.get(id);
    const url = `content/${id}.md`;
    const p = fetch(url, { cache: 'no-cache' })
      .then(r => r.ok ? r.text() : Promise.reject(new Error(`HTTP ${r.status}`)))
      .then(text => {
        const note = parseMdNote(id, text);
        NOTE_CACHE.set(id, note);
        return note;
      })
      .catch(err => {
        // Surface the underlying problem in devtools so the user knows whether
        // the file is missing, the server is misconfigured, or the page was
        // opened via file:// (where fetch is blocked by the browser).
        const isFile = location.protocol === 'file:';
        console.error(
          `[uzd] couldn't load ${url}:`, err && err.message || err,
          isFile ? '(opened via file:// — browsers block fetch; serve over http instead)' : ''
        );
        MISSING.add(id);
        return null;
      })
      .finally(() => PENDING.delete(id));
    PENDING.set(id, p);
    return p;
  }

  function parseMdNote(id, text) {
    let front = {};
    let body = text;
    const fmMatch = text.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
    if (fmMatch) {
      front = parseFrontMatter(fmMatch[1]);
      body = fmMatch[2];
    }
    const accent = front.accent || (front.type === 'project' ? 'green' : 'indigo');
    const pills = parsePills(front.pills);
    return {
      id,
      type: front.type || 'note',
      kicker: front.kicker || front.type || id,
      title: front.title || id,
      subtitle: front.subtitle || front.tagline || '',
      accent,
      pills,
      images: Array.isArray(front.images) ? front.images.filter(Boolean) : [],
      body: [{ kind: 'md', html: mdToHtml(body) }],
      _md: true,
    };
  }

  function parseFrontMatter(s) {
    const out = {};
    const lines = s.split(/\r?\n/);
    let currentKey = null;
    for (const raw of lines) {
      if (!raw.trim()) { continue; }
      const indented = /^\s+/.test(raw);
      const line = raw.replace(/\r$/, '');
      if (indented && currentKey) {
        const v = line.trim().replace(/^-\s*/, '');
        if (!Array.isArray(out[currentKey])) out[currentKey] = [];
        out[currentKey].push(unquote(v));
        continue;
      }
      const km = line.match(/^([\w-]+):\s*(.*)$/);
      if (!km) continue;
      const k = km[1];
      const v = km[2].trim();
      if (!v) {
        currentKey = k;
        out[k] = [];
      } else if (v.startsWith('[') && v.endsWith(']')) {
        out[k] = v.slice(1, -1).split(',').map(x => unquote(x.trim())).filter(Boolean);
        currentKey = null;
      } else {
        out[k] = unquote(v);
        currentKey = null;
      }
    }
    return out;
  }

  function unquote(s) {
    s = s.trim();
    if (s.length >= 2 && ((s[0] === '"' && s[s.length-1] === '"') || (s[0] === "'" && s[s.length-1] === "'"))) {
      return s.slice(1, -1);
    }
    return s;
  }

  function parsePills(raw) {
    if (!raw) return [];
    const arr = Array.isArray(raw) ? raw : [raw];
    return arr.map(item => {
      const parts = String(item).split('|').map(s => s.trim());
      const out = { label: parts[0] };
      if (parts[1]) out.variant = parts[1];
      return out;
    }).filter(p => p.label);
  }

  function escHtml(s) {
    return String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
  }
  function escAttr(s) {
    return escHtml(s).replace(/"/g, '&quot;');
  }

  function parseTableRow(line) {
    // Strip optional leading/trailing pipe and split on | (no escaping needed
    // for our content set; if a cell needs a literal pipe, use &#124;).
    let s = line.trim();
    if (s.startsWith('|')) s = s.slice(1);
    if (s.endsWith('|')) s = s.slice(0, -1);
    return s.split('|').map(c => c.trim());
  }

  function inlineMd(text) {
    // Tokenise inline. Order: escape, code, image, link, bold, italic.
    // We escape the whole string first, then do replacements. Markdown emphasis
    // uses literal * which survives escaping.
    let s = escHtml(text);
    s = s.replace(/`([^`]+)`/g, (_, c) => `<code>${c}</code>`);
    s = s.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, (_, alt, src) =>
      `<img src="${escAttr(src)}" alt="${escAttr(alt)}">`);
    s = s.replace(/\[([^\]]+)\]\(([^)]+)\)/g, (_, label, href) => {
      const isExternal = /^(?:https?:|mailto:)/.test(href);
      if (isExternal) {
        const tgt = href.startsWith('mailto:') ? '' : ' target="_blank" rel="noopener"';
        return `<a href="${escAttr(href)}"${tgt}>${label}</a>`;
      }
      // Internal: a slug. Strip leading slashes/hashes and any .md suffix,
      // then map /<section>/<slug> → <section-singular>-<slug>, the convention
      // used in content/<prefix>-<slug>.md.
      const path = href.replace(/^[#/]+/, '').replace(/\.md$/, '');
      let noteId;
      if (path.includes('/')) {
        const idx = path.indexOf('/');
        const section = path.slice(0, idx);
        const slug = path.slice(idx + 1);
        const sectionMap = {
          projects: 'project',
          manifesto: 'manifesto',
          team: 'team',
          partners: 'partner',
          curiosities: 'curiosity',
        };
        const prefix = sectionMap[section];
        noteId = prefix ? `${prefix}-${slug}` : slug;
      } else {
        noteId = path;
      }
      return `<a href="?n=${escAttr(noteId)}" data-note="${escAttr(noteId)}">${label}</a>`;
    });
    s = s.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');
    s = s.replace(/(^|[^*])\*([^*\n]+)\*(?!\*)/g, '$1<em>$2</em>');
    return s;
  }

  function mdToHtml(md) {
    const out = [];
    const lines = md.split(/\r?\n/);
    let i = 0;
    while (i < lines.length) {
      const line = lines[i];
      if (!line.trim()) { i++; continue; }

      // Skip a top-level H1; the title is taken from front matter.
      const h = line.match(/^(#{1,4})\s+(.*?)\s*$/);
      if (h) {
        const lvl = h[1].length;
        if (lvl > 1) out.push(`<h${lvl}>${inlineMd(h[2])}</h${lvl}>`);
        i++;
        continue;
      }

      if (/^-{3,}\s*$/.test(line)) { out.push('<hr>'); i++; continue; }

      if (line.startsWith('>')) {
        const buf = [];
        while (i < lines.length && lines[i].startsWith('>')) {
          buf.push(lines[i].replace(/^>\s?/, ''));
          i++;
        }
        out.push(`<blockquote>${inlineMd(buf.join(' '))}</blockquote>`);
        continue;
      }

      if (/^[-*]\s+/.test(line)) {
        const items = [];
        while (i < lines.length && /^[-*]\s+/.test(lines[i])) {
          items.push(`<li>${inlineMd(lines[i].replace(/^[-*]\s+/, ''))}</li>`);
          i++;
        }
        out.push(`<ul>${items.join('')}</ul>`);
        continue;
      }

      if (/^\d+\.\s+/.test(line)) {
        const items = [];
        while (i < lines.length && /^\d+\.\s+/.test(lines[i])) {
          items.push(`<li>${inlineMd(lines[i].replace(/^\d+\.\s+/, ''))}</li>`);
          i++;
        }
        out.push(`<ol>${items.join('')}</ol>`);
        continue;
      }

      // Image-only paragraph
      const im = line.match(/^!\[([^\]]*)\]\(([^)]+)\)\s*$/);
      if (im) {
        const alt = im[1], src = im[2];
        out.push(`<figure><img src="${escAttr(src)}" alt="${escAttr(alt)}">${alt ? `<figcaption>${escHtml(alt)}</figcaption>` : ''}</figure>`);
        i++;
        continue;
      }

      // GFM-style table: header row, separator row of |---|, then data rows.
      if (line.includes('|') && i + 1 < lines.length) {
        const sep = lines[i + 1];
        if (sep && /\|/.test(sep) && /^[\s|:\-]+$/.test(sep) && /-/.test(sep)) {
          const headers = parseTableRow(line);
          i += 2; // header + separator
          const rows = [];
          while (i < lines.length && lines[i].trim() && lines[i].includes('|')) {
            rows.push(parseTableRow(lines[i]));
            i++;
          }
          const thead = '<thead><tr>' + headers.map(h => `<th>${inlineMd(h)}</th>`).join('') + '</tr></thead>';
          const tbody = '<tbody>' + rows.map(r =>
            '<tr>' + r.map(c => `<td>${inlineMd(c)}</td>`).join('') + '</tr>'
          ).join('') + '</tbody>';
          out.push(`<table>${thead}${tbody}</table>`);
          continue;
        }
      }

      // Raw HTML block: a line beginning with an HTML tag (e.g. <iframe>,
      // <video>, <details>, <div>). We trust the markdown source — the user
      // is the author — and pass these through verbatim until the next blank
      // line. inlineMd is not applied (HTML attributes break the inline
      // tokeniser, and we don't want them mangled).
      if (/^<\s*[a-zA-Z]/.test(line)) {
        const buf = [line];
        i++;
        while (i < lines.length && lines[i].trim() !== '') {
          buf.push(lines[i]);
          i++;
        }
        out.push(buf.join('\n'));
        continue;
      }

      // Paragraph: gather until a blank line or a structural marker.
      const buf = [line];
      i++;
      while (i < lines.length && lines[i].trim() &&
             !/^(#{1,4}\s|>|-{3,}\s*$|[-*]\s|\d+\.\s|!\[|\||<\s*[a-zA-Z])/.test(lines[i])) {
        buf.push(lines[i]);
        i++;
      }
      out.push(`<p>${inlineMd(buf.join(' '))}</p>`);
    }
    // First paragraph becomes the lede.
    for (let j = 0; j < out.length; j++) {
      if (out[j].startsWith('<p>')) {
        out[j] = `<p class="lede">${out[j].slice(3, -4)}</p>`;
        break;
      }
    }
    return out.join('\n');
  }

