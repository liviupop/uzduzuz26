  function renderPlaceholder(id, depth, isActive) {
    // Shown while content/<id>.md is loading, or if it doesn't exist.
    const note = el('section', {
      class: 'note ' + (isActive ? 'is-active' : 'is-spine'),
      'data-id': id,
      'data-accent': 'indigo',
    });
    if (!isActive) {
      const sh = el('div', { class: 'spine-head' });
      const closeBtn = el('button', { class: 'spine-close', 'aria-label': 'close note' }, '×');
      closeBtn.addEventListener('click', (e) => { e.stopPropagation(); app.closeOne(depth); });
      sh.appendChild(closeBtn);
      sh.appendChild(el('div', { class: 'spine-kicker' }, 'loading'));
      sh.appendChild(el('div', { class: 'spine-label' }, id));
      sh.appendChild(el('div', { class: 'spine-num' }, String(depth + 1).padStart(2, '0')));
      note.appendChild(sh);
      // Spine click is delegated on $stackInner.
      return note;
    }
    if (depth > 0) {
      const closeBtn = el('button', { class: 'note-close', 'aria-label': 'close' }, '×');
      closeBtn.addEventListener('click', () => app.closeOne(depth));
      note.appendChild(closeBtn);
    }
    const inner = el('div', { class: 'note-inner' });
    const head = el('div', { class: 'note-head' });
    head.appendChild(el('div', { class: 'note-kicker' }, MISSING.has(id) ? 'not found' : 'loading'));
    head.appendChild(el('h1', { class: 'note-title' }, MISSING.has(id) ? id : 'Loading…'));
    if (MISSING.has(id)) {
      const isFile = location.protocol === 'file:';
      const tip = isFile
        ? "This page was opened from a file:// URL. Browsers block fetch on file://, so the markdown notes can't be loaded. Run a local server (cd uzduz25 && python3 -m http.server 8080) and open http://localhost:8080 instead."
        : `Tried to load content/${id}.md but the request failed. Check the server is running from the uzduz25/ directory, and that the file exists. See the browser console for details.`;
      head.appendChild(el('p', { class: 'note-subtitle' }, tip));
    }
    inner.appendChild(head);
    note.appendChild(inner);
    return note;
  }

  // Slideshow: a sticky-right-column auto-advancing carousel of images for a
  // note. Images are stored at /assets/images/<slug>/<filename>; the note's
  // front matter declares the order via `images: [...]`. Auto-advances every
  // 5 s; pauses on hover; prev/next arrows; dot indicators below.
  function buildSlideshow(slug, images) {
    const wrap = el('div', { class: 'note-slideshow', 'data-slug': slug });
    const stage = el('div', { class: 'slideshow-stage' });
    const imgEls = images.map((name, i) => {
      const src = `assets/images/${slug}/${name}`;
      const im = el('img', {
        class: 'slideshow-img' + (i === 0 ? ' is-active' : ''),
        src,
        alt: '',
        loading: i === 0 ? 'eager' : 'lazy',
        decoding: 'async',
      });
      stage.appendChild(im);
      return im;
    });
    wrap.appendChild(stage);

    if (images.length > 1) {
      const prev = el('button', { class: 'slideshow-arrow prev', type: 'button', 'aria-label': 'previous image' }, '‹');
      const next = el('button', { class: 'slideshow-arrow next', type: 'button', 'aria-label': 'next image' }, '›');
      const dots = el('div', { class: 'slideshow-dots' });
      const dotEls = images.map((_, i) => {
        const d = el('button', {
          class: 'slideshow-dot' + (i === 0 ? ' is-active' : ''),
          type: 'button',
          'aria-label': `image ${i + 1}`,
          'data-i': String(i),
        });
        dots.appendChild(d);
        return d;
      });

      wrap.appendChild(prev);
      wrap.appendChild(next);
      wrap.appendChild(dots);

      let idx = 0;
      let timer = null;
      const setActive = (i) => {
        idx = (i + images.length) % images.length;
        imgEls.forEach((e, k) => e.classList.toggle('is-active', k === idx));
        dotEls.forEach((e, k) => e.classList.toggle('is-active', k === idx));
      };
      const advance = () => setActive(idx + 1);
      const start = () => { if (!timer) timer = setInterval(advance, 5000); };
      const stop = () => { if (timer) { clearInterval(timer); timer = null; } };

      prev.addEventListener('click', () => { setActive(idx - 1); stop(); start(); });
      next.addEventListener('click', () => { setActive(idx + 1); stop(); start(); });
      dotEls.forEach((d) => d.addEventListener('click', () => {
        setActive(parseInt(d.dataset.i, 10)); stop(); start();
      }));
      wrap.addEventListener('mouseenter', stop);
      wrap.addEventListener('mouseleave', start);
      // Cleanup when the note's column is removed from the DOM. Uses a
      // MutationObserver up the tree to detect detachment.
      const obs = new MutationObserver(() => {
        if (!wrap.isConnected) { stop(); obs.disconnect(); }
      });
      obs.observe(document.body, { childList: true, subtree: true });
      start();
    }

    return wrap;
  }

  function renderNote(id, depth, isActive) {
    const data = getNote(id);
    if (!data) return renderPlaceholder(id, depth, isActive);

    const note = el('section', {
      class: 'note ' + (isActive ? 'is-active' : 'is-spine'),
      'data-id': id,
      'data-accent': data.accent || 'indigo',
    });

    if (!isActive) {
      // Spine layout: rotated label + close button
      const spineHead = el('div', { class: 'spine-head' });
      const closeBtn = el('button', { class: 'spine-close', 'aria-label': 'close note' }, '×');
      closeBtn.addEventListener('click', (e) => { e.stopPropagation(); app.closeOne(depth); });
      spineHead.appendChild(closeBtn);

      const kicker = data.kicker || data.type || id;
      spineHead.appendChild(el('div', { class: 'spine-kicker' }, kicker));

      const labelText = (data.title || id).replace(/\*/g, '');
      spineHead.appendChild(el('div', { class: 'spine-label' }, labelText));

      spineHead.appendChild(el('div', { class: 'spine-num' }, String(depth + 1).padStart(2, '0')));

      note.appendChild(spineHead);
      // Click-to-activate is handled via delegation on $stackInner so it
      // survives in-place rebuilds.
      return note;
    }

    // ------ ACTIVE note (full content) ------

    note.appendChild(el('div', { class: 'note-depth' }));

    // close button (top right) — only shown on non-home active notes
    if (depth > 0) {
      const closeBtn = el('button', { class: 'note-close', 'aria-label': 'close' }, '×');
      closeBtn.addEventListener('click', () => app.closeOne(depth));
      note.appendChild(closeBtn);
    }

    const inner = el('div', { class: 'note-inner' });

    // ---- header ----
    const head = el('div', { class: 'note-head' });

    // homepage-only flourish
    if (id === 'home') {
      const flo = el('div', { class: 'cover-flourish' });
      flo.appendChild(el('div', { class: 'yr' }, 'EST. 2008 · REBOOTED 2020 · CLUJ-NAPOCA'));
      flo.appendChild(el('div', { class: 'stamp' }, ['a factory', el('br'), 'by the dozen']));
      head.appendChild(flo);
    } else {
      head.appendChild(el('div', { class: 'note-kicker' }, data.kicker || data.type));
    }

    head.appendChild(el('h1', { class: 'note-title' }, [emphasize(data.title)]));

    if (data.subtitle) {
      head.appendChild(el('p', { class: 'note-subtitle' }, data.subtitle));
    }

    if (data.pills && data.pills.length) {
      const pills = el('div', { class: 'note-pills' });
      for (const p of data.pills) {
        pills.appendChild(el('span', { class: 'note-pill' + (p.variant === 'on' ? ' on' : '') }, p.label));
      }
      head.appendChild(pills);
    }

    // For principle notes, place a prominent "next principle →" button in the
    // header. Loops back to principle 01 after the last one.
    if (data.type === 'principle' && AUTO_INDEX) {
      const principles = AUTO_INDEX
        .filter(n => n.type === 'principle')
        .sort((a, b) => a.order - b.order || a.slug.localeCompare(b.slug));
      const idx = principles.findIndex(n => n.slug === id);
      if (idx >= 0) {
        const next = principles[(idx + 1) % principles.length];
        const btn = el('a', {
          class: 'note-nav-next',
          href: `?n=${next.slug}`,
          'data-note': next.slug,
        }, [
          el('span', { class: 'lbl' }, `next: ${next.title}`),
          el('span', { class: 'arr' }, '→'),
        ]);
        btn.addEventListener('click', (e) => {
          e.preventDefault();
          app.openNoteAt(next.slug, depth);
        });
        head.appendChild(btn);
      }
    }

    inner.appendChild(head);

    // ---- body ----
    const body = el('div', { class: 'note-body' });
    const ctx = { depth, openNote: (target, fromDepth) => app.openNoteAt(target, fromDepth) };

    for (const block of data.body || []) {
      const node = renderBlock(block, ctx);
      if (node) body.appendChild(node);
    }

    inner.appendChild(body);

    // ---- footer micro ----
    const foot = el('div', { class: 'note-foot' }, [
      el('span', {}, `note · ${data.kicker || id}`),
      el('a', { href: '?n=contact', 'data-note': 'contact', class: 'note-foot-contact' }, 'contact'),
      el('span', {}, depth === 0 ? 'click any link to stack →' : `depth ${depth + 1}`),
    ]);
    foot.querySelectorAll('a[data-note]').forEach(a => {
      a.addEventListener('click', (e) => {
        e.preventDefault();
        app.openNoteAt(a.dataset.note, depth);
      });
    });
    inner.appendChild(foot);

    // The slideshow lives OUTSIDE the .note-inner max-width box so the body
    // keeps its full editorial width. Wraps inner + slideshow in a flex row.
    const hasSlideshow = Array.isArray(data.images) && data.images.length > 0;
    if (hasSlideshow) {
      const flex = el('div', { class: 'note-flex' });
      flex.appendChild(inner);
      flex.appendChild(buildSlideshow(id, data.images));
      note.appendChild(flex);
    } else {
      note.appendChild(inner);
    }

    return note;
  }

