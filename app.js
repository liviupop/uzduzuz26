  function renderBlock(b, ctx) {
    switch (b.kind) {
      case 'md': {
        const wrap = el('div', { class: 'b-md', html: b.html });
        // Wire internal links to the stack router.
        wrap.querySelectorAll('a[data-note]').forEach(a => {
          a.addEventListener('click', (e) => {
            e.preventDefault();
            ctx.openNote(a.dataset.note, ctx.depth);
          });
        });
        return wrap;
      }
      case 'constellation': {
        return renderConstellation(ctx);
      }
      case 'auto-list': {
        // Filter the auto-index by `b.filter` (an object whose keys must match
        // each note's front-matter values; values may be a string or array of
        // accepted strings). Sort by `order` ascending then by `slug`.
        // Render rows that look like the project-list block.
        const wrap = el('div', { class: 'b-project-list b-auto-list' });
        const placeholder = el('div', { class: 'b-md', html:
          '<p class="lede" style="opacity: 0.6">Loading…</p>' });
        wrap.appendChild(placeholder);
        loadAutoIndex().then(idx => {
          if (!wrap.isConnected) return;
          wrap.innerHTML = '';
          const matches = idx
            .filter(n => matchesFilter(n, b.filter || {}))
            .filter(n => !(b.exclude || []).includes(n.slug))
            .sort((a, c) => {
              if (a.order !== c.order) return a.order - c.order;
              return a.slug.localeCompare(c.slug);
            });
          if (matches.length === 0) {
            wrap.appendChild(el('div', { class: 'b-md', html:
              '<p style="opacity: 0.6">No notes match this filter yet.</p>' }));
            return;
          }
          matches.forEach((n, i) => {
            const row = el('div', { class: 'proj-row' });
            row.addEventListener('click', () => ctx.openNote(n.slug, ctx.depth));
            const num = b.numbered === false ? null
              : String(typeof n.order === 'number' && n.order < 99 ? n.order : i + 1).padStart(2, '0');
            if (num) row.appendChild(el('div', { class: 'n' }, num));
            const body = el('div', {});
            body.appendChild(el('div', { class: 'ttl' }, n.title || n.slug));
            // Tagline: only use the explicit `summary` field. The auto-extracted
            // `excerpt` stays in the index for other uses but we don't fall back
            // to it here — keeps the index visually clean.
            if (n.summary) body.appendChild(el('div', { class: 'tagline' }, n.summary));
            if (n.kicker) body.appendChild(el('div', { class: 'sub' }, n.kicker));
            row.appendChild(body);
            wrap.appendChild(row);
          });
        });
        return wrap;
      }
      case 'lede': return el('p', { class: 'b-lede' }, b.text);
      case 'p': return el('p', { class: 'b-p' }, b.text);
      case 'h2': return el('h2', { class: 'b-h2' }, [b.text]);
      case 'callout': return el('div', { class: 'b-callout' }, b.text);
      case 'callout-cta': {
        const wrap = el('div', { class: 'b-callout-cta' });
        wrap.appendChild(el('p', {}, b.text));
        if (b.email) {
          wrap.appendChild(el('a', { class: 'email', href: `mailto:${b.email}` }, b.email));
        }
        return wrap;
      }
      case 'shout': return el('div', { class: 'b-shout' }, b.text);
      case 'shout-quote': {
        const wrap = el('blockquote', { class: 'b-shout-quote' }, b.text);
        if (b.who) wrap.appendChild(el('span', { class: 'who' }, `— ${b.who}`));
        return wrap;
      }
      case 'tags': {
        const wrap = el('div', { class: 'b-tags' });
        for (const it of b.items) {
          wrap.appendChild(el('div', { class: 'tag-row' }, [
            el('span', { class: 't' }, it.tag),
            el('span', { class: 'x' }, it.text),
          ]));
        }
        return wrap;
      }
      case 'cards': {
        const wrap = el('div', { class: 'b-cards' });
        for (const it of b.items) {
          const klass = 'card-tile' + (it.href ? '' : ' is-static');
          const tile = el(it.href ? 'a' : 'div', { class: klass });
          if (it.href) {
            tile.addEventListener('click', (e) => { e.preventDefault(); ctx.openNote(it.href, ctx.depth); });
          }
          tile.appendChild(el('span', { class: 'lbl' }, it.label));
          if (it.sub) tile.appendChild(el('span', { class: 'sub' }, it.sub));
          wrap.appendChild(tile);
        }
        return wrap;
      }
      case 'principle': {
        const wrap = el('div', { class: 'b-principle' });
        wrap.appendChild(el('div', { class: 'num' }, b.n));
        const pbody = el('div', { class: 'pbody' });
        pbody.appendChild(el('h3', {}, b.title));
        pbody.appendChild(el('p', { class: 'ptext' }, b.text));
        if (b.example) pbody.appendChild(el('div', { class: 'pex' }, b.example));
        wrap.appendChild(pbody);
        return wrap;
      }
      case 'numbered': {
        const wrap = el('div', { class: 'b-numbered' });
        for (const it of b.items) {
          const row = el('div', { class: 'num-row' });
          row.appendChild(el('div', { class: 'n' }, it.n));
          const body = el('div', {});
          body.appendChild(el('div', { class: 'ttl' }, it.title));
          if (it.sub) body.appendChild(el('div', { class: 'sub' }, it.sub));
          row.appendChild(body);
          wrap.appendChild(row);
        }
        return wrap;
      }
      case 'timeline': {
        const wrap = el('div', { class: 'b-timeline' });
        for (const it of b.items) {
          wrap.appendChild(el('div', { class: 'tl-row' }, [
            el('div', { class: 'yr' }, it.year),
            el('div', { class: 'txt' }, it.text),
          ]));
        }
        return wrap;
      }
      case 'project-list': {
        const wrap = el('div', { class: 'b-project-list' });
        for (const it of b.items) {
          const row = el('div', { class: 'proj-row' });
          row.addEventListener('click', () => ctx.openNote(it.href, ctx.depth));
          row.appendChild(el('div', { class: 'n' }, it.n));
          const body = el('div', {});
          body.appendChild(el('div', { class: 'ttl' }, it.title));
          if (it.tagline) body.appendChild(el('div', { class: 'tagline' }, it.tagline));
          if (it.sub) body.appendChild(el('div', { class: 'sub' }, it.sub));
          row.appendChild(body);
          wrap.appendChild(row);
        }
        return wrap;
      }
      case 'tag-strip': {
        const wrap = el('div', { class: 'b-tag-strip' });
        for (const t of b.items) wrap.appendChild(el('span', { class: 'tag-chip' }, t));
        return wrap;
      }
      case 'links': {
        const wrap = el('div', { class: 'b-links' });
        for (const it of b.items) {
          const row = el('a', { class: 'link-row' }, it.label);
          if (it.href && it.href !== '#') {
            row.addEventListener('click', (e) => { e.preventDefault(); ctx.openNote(it.href, ctx.depth); });
          }
          wrap.appendChild(row);
        }
        return wrap;
      }
      case 'signers': {
        const wrap = el('div', { class: 'b-signers' });
        for (const s of b.items) wrap.appendChild(el('div', { class: 'signer' }, s));
        return wrap;
      }
      case 'addresses': {
        const wrap = el('div', { class: 'b-addresses' });
        for (const it of b.items) {
          const valueNode =
            typeof it.value === 'string' && /<\s*[a-z!/]/i.test(it.value)
              ? el('div', { class: 'val', html: it.value })
              : el('div', { class: 'val' }, it.value);
          wrap.appendChild(el('div', { class: 'addr-row' }, [
            el('div', { class: 'lbl' }, it.label),
            valueNode,
          ]));
        }
        wrap.querySelectorAll('a[data-note]').forEach(a => {
          a.addEventListener('click', (e) => {
            e.preventDefault();
            ctx.openNote(a.dataset.note, ctx.depth);
          });
        });
        return wrap;
      }
      default: return null;
    }
  }

