  function setStackUrl(ids, activeIndex, mode) {
    const params = new URLSearchParams(location.search);
    if (ids.length === 1 && ids[0] === 'home') {
      params.delete('n');
    } else {
      params.set('n', ids.join(','));
    }
    // Only encode the active index when it's not the default (the last column).
    if (typeof activeIndex === 'number' && activeIndex !== ids.length - 1) {
      params.set('a', String(activeIndex));
    } else {
      params.delete('a');
    }
    const q = params.toString();
    const url = q ? `?${q}` : location.pathname;
    // Skip the write if the URL is already correct (avoids dummy entries
    // when async re-renders fire after the user has already pushed state).
    const current = location.pathname + (location.search || '');
    if (current === url) return;
    if (mode === 'replace') history.replaceState({ stack: ids.slice(), activeIndex }, '', url);
    else history.pushState({ stack: ids.slice(), activeIndex }, '', url);
  }

