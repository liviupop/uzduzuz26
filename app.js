    function isHl(kind, slug) {
      const focus = focusedSlug();
      if (!focus) return false;
      const fp = partners.find(p => p.slug === focus);
      const fr = projects.find(p => p.slug === focus);
      if (fp) {
        if (kind === 'partner') return slug === focus;
        if (kind === 'project') return fp.projects.includes(slug);
      }
      if (fr) {
        if (kind === 'project') return slug === focus;
        if (kind === 'partner') return fr.partners.includes(slug);
      }
      return false;
    }

