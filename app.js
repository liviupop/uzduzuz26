  function getStackFromUrl() {
    const params = new URLSearchParams(location.search);
    const raw = (params.get('n') || 'home').split(',').filter(Boolean);
    const stack = raw.length ? raw : ['home'];
    // Active column is the last by default; ?a=N (0-based) overrides.
    let activeIndex = stack.length - 1;
    const aRaw = params.get('a');
    if (aRaw !== null) {
      const n = parseInt(aRaw, 10);
      if (!isNaN(n) && n >= 0 && n < stack.length) activeIndex = n;
    }
    return { stack, activeIndex };
  }

  function arraysEqual(a, b) {
    if (a.length !== b.length) return false;
    for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return false;
    return true;
  }

  // Simple filter for the auto-list block. The filter is an object like
  // { type: 'project', role: ['lead','partner'] }. A note matches if every
  // key's value matches (string equality, or membership when the filter
  // value is an array).
  function matchesFilter(note, filter) {
    for (const k of Object.keys(filter)) {
      const want = filter[k];
      const got = note[k];
      if (Array.isArray(want)) {
        if (!want.includes(got)) return false;
      } else if (got !== want) {
        return false;
      }
    }
    return true;
  }

